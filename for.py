for i in range(1, 5):
    print(i)
else:
    print('for loop is over')

# in range(start number, end numberm, step length(default 1))
# the end number can not be achieved
# i in range(1, 5), the values of i includes 1, 2, 3, 4
# similar to `for (int i=1; i<5; i++)`
# j in range(1, 5, 2), the values of j includes 1, 3
# list(range(5)) -> [0, 1, 2, 3, 4]